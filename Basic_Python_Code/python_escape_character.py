txt_1 = "We are the so-called \"Vikings\" from the north."
print(txt_1)

txt_2 = 'It\'s alright.'
print(txt_2)

txt_3 = "This will insert one \\ (backslash)."
print(txt_3)

txt_4 = "Hello\nWorld!"
print(txt_4)

txt_5 = "Hello\rWorld!"
print(txt_5)

txt_6 = "Hello\tWorld!"
print(txt_6)

#A backslash followed by three integers will result in a octal value:
txt_7 = "\110\145\154\154\157"
print(txt_7)

#A backslash followed by an 'x' and a hex number represents a hex value:
txt_8 = "\x48\x65\x6c\x6c\x6f"
print(txt_8)
